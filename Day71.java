package Coding100Hari;

public class Hari71 {

    public static int Penjumlahan(int angka, int angka1) {
        return angka + angka1;
    }

    public static void main(String[] args) {
        int angka = 2;
        int angka1 = 1;

        int hasil = Penjumlahan(angka, angka1);
        System.out.println(hasil);

    }

}

package Coding100Hari;
public class Hari36 {
    public static void main(String[] args) {
        //operator aritmatika : perkalian, pembagian, modulo
        
        int nilai1 = 6;
        int nilai2 = 2;
        
        // perkalian 
        int perkalian = nilai1 * nilai2;
        System.out.println("Hasil perkalian = " + perkalian);
        
        //pembagian
        int pembagian = nilai1 / nilai2;
        System.out.println("Hasil pembagian = " + pembagian);
        
        //Modulo
        int sisaBagi = nilai1 % nilai2;
        System.out.println("Hasil sisa bagi = " + sisaBagi);
    }
    
}

public class Soal2 {
    public static void main(String[] args) {
        
        int a = 15;
        int b = 20;
        System.out.println(b);
        
        //20 - 15
        b -= a;
        System.out.println(b);
        
        //5 X 15
        b *= a;
        System.out.println(b); 
        
        //75 - 15
        b -= a;
        System.out.println(b);
        
        //60 / 15
        b /= a;
        System.out.println(b);
        
        //4 + 15
        b += a;
        System.out.println(b);
        
    }
    
}


package Coding100Hari;
public class Hari69 {
    public int perkalian(int angka1, int angka2){
        return angka1 * angka2;
        
    }
    public static void main(String[] args) {
        Hari69 kali = new Hari69();
        
        int mengalikan = kali.perkalian(2, 2);
        
        System.out.println("Hasil perkalian :" + mengalikan);
    }
    
}

package Coding100Hari;
public class Hari22 {
    public static void main(String[] args) {
        //operator penugasan: penjumlahan dan pengurangan
        
        //Penjumlahan
        int x = 10;
        x += 10;
        
        //Pengurangan
        int y = 8;
        y -= 2;
        
        System.out.println("Penjumlahan : " + x);
        System.out.println("Pengurangan : " + y);
    }
    
}

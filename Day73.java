package Coding100Hari;
public class Hari73 {
    public static void main(String[] args) {
        String kata = "Atisa";
        
        //memanggil huruf pertama
        char huruf1 = kata.charAt(0);
        System.out.println("huruf pertama :" + huruf1);
        
        //memanggil huruf tengah
        char huruf2 = kata.charAt(2);
        System.out.println("huruf tengah :" + huruf2);
        
        //memanggil huruf terakhir
        char huruf3 = kata.charAt(4);
        System.out.println("huruf terakhir :" + huruf3);
    }
    
}


package Coding100Hari;

public class Hari40 {
    public static void main(String[] args) {
        //operator perbandingan
        int nilai1 = 12;
        int nilai2 = 2;
        
        nilai1 += 3;
        nilai2 += 3;
        
        System.out.println("nilai1 setelah ditambah 3 =" + nilai1);
        System.out.println("nilai2 setelah ditambah 3 =" + nilai2);
        
        //membandigkan nilai1 dengan nilai2
        System.out.println(nilai1 > nilai2);
        System.out.println(nilai1 < nilai2);
    }  
}

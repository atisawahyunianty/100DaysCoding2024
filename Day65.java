package Coding100Hari;
public class Hari65 {
    public static void main(String[] args) {
        int tisa = 3;
        int atisa = 6;
        
        for (int i = 1; i <= tisa; i++) { //loop baris 
            for (int j = 1; j <= atisa; j++) { //loop kolom
                System.out.print( j + " ");
                
            }
            System.out.println();
            
        }
    }
    
}

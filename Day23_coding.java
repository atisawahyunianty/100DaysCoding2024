package Coding100Hari;
public class Hari23 {
    public static void main(String[] args) {
        //operator penugasan : perkalian, pembagian, modulus
        
        //perkalian
        byte a = 6;
        a *= 2;
        
        //pembagian
        int b = 50;
        b /= 2;
        
        //modulus
        short c = 13;
        c %= 3;
        
        System.out.println("Hasil perkaian :" + a);
        System.out.println("Hasil pembagian :" + b);
        System.out.println("Hasil Modulus atau sisa bagi :" + c);
        
    }
    
}
